public class forLoopDemo {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i++)   {
            System.out.println("The current value of i: " + i);
        }
        for (int i = 10; i >= 0; i--)   {
            System.out.println("The current value of i: " + i);
        }
        for (int i = 10; i >= 0; i -= 2)   {
            System.out.println("The current value of i: " + i);
        }

        for (int i = 1; i <= 4; i ++)   {
            System.out.println("2 times " + i + " = " + (2*i));
        }

    }
}
